/**
 * op_add - addition
 * @a: integer number 1
 * @b: integer number 2
 * Return: result of operation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: integer number 1
 * @b: integer number 2
 * Return: result of operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: integer number 1
 * @b: integer number 2
 * Return: result of multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: integer number 1
 * @b: integer number 2
 * Return: result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division
 * @a: integer number 1
 * @b: integer number 2
 * Return: result of modulo
 */

int op_mod(int a, int b)
{
	return (a % b);
}
